// server/index.js

// ‚îÄ‚îÄ Load environment variables from server/.env ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
require('dotenv').config();

const express    = require('express');   // Express framework
const mongoose   = require('mongoose');  // MongoDB ODM
const cors       = require('cors');      // Enable CORS
const cookieParser = require('cookie-parser'); // ‚Üê Import for parsing cookies

// ‚îÄ‚îÄ Import route modules (CommonJS) ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
const authRoutes  = require('./routes/auth');
const skillRoutes = require('./routes/skillRoutes');
const userRoutes  = require('./routes/user');
const jobRoutes   = require('./routes/jobRoutes');
const contactRoutes = require('./routes/contactRoutes');


// ‚îÄ‚îÄ JWT auth middleware ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
const verifyToken = require('./middleware/verifyToken');

const app = express();

// ‚îÄ‚îÄ GLOBAL MIDDLEWARE ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
app.use(cors({
  origin: 'http://localhost:5173',    // ‚Üê your client URL
  credentials: true                   // ‚Üê allow cookies to be sent
}));
app.use(express.json());               // parse JSON bodies
app.use(cookieParser());               // ‚Üê parse cookies on incoming requests

// ‚îÄ‚îÄ PUBLIC ROUTES ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
// Health‚Äëcheck endpoint
app.get('/', (req, res) => res.send('‚úÖ Job Tracker API is up and running'));

// Auth routes (register & login)‚Äîno token required
app.use('/api/auth', authRoutes);

// ‚îÄ‚îÄ PROTECTED ROUTES ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
// Skill and user routes require a valid JWT
app.use('/api/skills', verifyToken, skillRoutes);
app.use('/api/user',   verifyToken, userRoutes);

// Job routes for CRUD operations‚Äîalso protected
app.use('/api/jobs', verifyToken, jobRoutes);

//Contact Routes for CRUD operations also protected
app.use('/api/contacts', verifyToken, contactRoutes);

// Example inline protected endpoint
app.get('/api/protected', verifyToken, (req, res) => {
  res.json({ message: `Hello user ${req.userId}, you have access!` });
});

// ‚îÄ‚îÄ DATABASE CONNECTION & SERVER STARTUP ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
mongoose
  .connect(process.env.MONGO_URI, {
    dbName: 'test'     // explicitly use the 'test' database
  })
  .then(() => {
    const PORT = process.env.PORT || 5009;
    app.listen(PORT, () => {
      console.log(`üöÄ Server listening on http://localhost:${PORT}`);
    });
  })
  .catch(err => {
    console.error('‚ùå MongoDB connection error:', err);
    process.exit(1);
  });
